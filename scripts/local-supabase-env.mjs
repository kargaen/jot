import { execFileSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT_DIR = resolve(fileURLToPath(new URL("..", import.meta.url)));
export const GENERATED_ENV_PATH = resolve(ROOT_DIR, ".env.e2e.local");

const DEFAULT_TEST_EMAIL = "e2e@local.jot";
const DEFAULT_TEST_PASSWORD = "JotE2E!234";
const DEFAULT_AUTH_REDIRECT_URL = "http://127.0.0.1:1420/confirmed.html";

function npxCommand() {
  return process.platform === "win32" ? "npx.cmd" : "npx";
}

function parseKeyValueLines(text) {
  return Object.fromEntries(
    text
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter(Boolean)
      .map((line) => {
        const separator = line.indexOf("=");
        return [line.slice(0, separator), line.slice(separator + 1)];
      }),
  );
}

export function parseDotEnvFile(path) {
  const text = readFileSync(path, "utf8");
  return parseKeyValueLines(text);
}

export function resolveLocalSupabaseEnv() {
  try {
    const output = execFileSync(
      npxCommand(),
      ["supabase", "status", "-o", "env"],
      {
        cwd: ROOT_DIR,
        encoding: "utf8",
        stdio: ["ignore", "pipe", "pipe"],
      },
    );

    const raw = parseKeyValueLines(output);
    const apiUrl = raw.API_URL ?? raw.SUPABASE_URL;
    const anonKey = raw.ANON_KEY ?? raw.SUPABASE_ANON_KEY;
    const serviceRoleKey = raw.SERVICE_ROLE_KEY ?? raw.SUPABASE_SERVICE_ROLE_KEY;

    if (!apiUrl || !anonKey || !serviceRoleKey) {
      throw new Error(
        "Supabase status did not expose API_URL, ANON_KEY, and SERVICE_ROLE_KEY.",
      );
    }

    return {
      apiUrl,
      anonKey,
      serviceRoleKey,
      testEmail: DEFAULT_TEST_EMAIL,
      testPassword: DEFAULT_TEST_PASSWORD,
      authRedirectUrl: DEFAULT_AUTH_REDIRECT_URL,
    };
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error);
    throw new Error(
      [
        "Unable to resolve local Supabase environment.",
        "Make sure Docker Desktop is running, then start the local stack with `npm run db:start`.",
        message,
      ].join(" "),
    );
  }
}

export function writeLocalSupabaseEnvFile(env) {
  const lines = [
    "# Generated by scripts/local-supabase-env.mjs",
    `VITE_SUPABASE_URL=${env.apiUrl}`,
    `VITE_SUPABASE_ANON_KEY=${env.anonKey}`,
    `VITE_AUTH_REDIRECT_URL=${env.authRedirectUrl}`,
    `VITE_E2E_TEST_EMAIL=${env.testEmail}`,
    `VITE_E2E_TEST_PASSWORD=${env.testPassword}`,
    `E2E_SUPABASE_URL=${env.apiUrl}`,
    `E2E_SUPABASE_ANON_KEY=${env.anonKey}`,
    `E2E_SUPABASE_SERVICE_ROLE_KEY=${env.serviceRoleKey}`,
    `E2E_TEST_EMAIL=${env.testEmail}`,
    `E2E_TEST_PASSWORD=${env.testPassword}`,
    "",
  ];

  writeFileSync(GENERATED_ENV_PATH, lines.join("\n"), "utf8");
  return GENERATED_ENV_PATH;
}

export function ensureLocalSupabaseEnvFile() {
  const env = resolveLocalSupabaseEnv();
  writeLocalSupabaseEnvFile(env);
  return env;
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const env = ensureLocalSupabaseEnvFile();
  console.log(`Wrote ${GENERATED_ENV_PATH}`);
  console.log(`Using local API: ${env.apiUrl}`);
}
